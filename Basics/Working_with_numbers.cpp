#include<iostream>
#include<cmath>
using namespace std;
int main()
{
    int x=9,y=87,s=90;
    x++;//Increment Operator

    y--;//Decrement Operator

    s+=10;//Shorthand Operator(-=,+=,*=,/=)

    cout<<s<<endl;

    cout<<y<<endl;

    cout<<5+9<<endl;

    cout<<5*9<<endl;

    cout<<9/5<<endl;

    cout<<5-9<<endl;

    cout<<11%3<<endl;

    cout<<5+9*10<<endl; //PEMDAS Order of Operation

    cout<<(5+9)*10<<endl;

    cout<<x<<endl;

    cout<<5.0/9.0<<endl;

    cout<<pow(5,2)<<endl;

    cout<<sqrt(87)<<endl;

    cout<<sqrt(22.456)<<endl;

    cout<<round(56.7)<<endl;

    cout<<floor(36.5)<<endl;

    cout<<ceil(76.78)<<endl;

    cout<<fmax(56,78.9)<<endl;

    cout<<fmin(56,78.9)<<endl;

    
    return 0;
}